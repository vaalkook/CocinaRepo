    private void botonListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListaActionPerformed
         if (listaPedidos.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No hay platillos en la lista.");
        return;
    }
    Ordenes orden = new Ordenes(listaPedidos.size());
    LogicMeseros mesero = new LogicMeseros(orden, listaPedidos);
    LogicCocineros cocinero = new LogicCocineros(orden, listaPedidos);
    mesero.iniciarMesero();
    cocinero.iniciarCocinero();
    }//GEN-LAST:event_botonListaActionPerformed

