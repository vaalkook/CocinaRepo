    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        try {
        int numeroPlatillo = Integer.parseInt(txtPlatillo.getText());
        if (numeroPlatillo >= 1 && numeroPlatillo <= 5) {
            listaPedidos.add(numeroPlatillo);
            JOptionPane.showMessageDialog(this, "Platillo agregado exitosamente");
            txtPlatillo.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Ingresa un número entre 1 y 5.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingresa un número válido.");
    }
    }//GEN-LAST:event_botonAgregarActionPerformed

